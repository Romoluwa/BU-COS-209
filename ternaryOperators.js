//these are examples of Ternary operators
//Example1
const age = 18;

// Using ternary operator
const canPlayGame = age >= 16 ? "Yes, you can play" : "No, you cannot play";

console.log(canPlayGame); // Output: "Yes, you can vote"

//Example2
const marks = 85;

const grade = marks > 90 ? "A+" : marks > 80 ? "A" : marks > 70 ? "B" : "C";

console.log(grade); // Output: "A"
